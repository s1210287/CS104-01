#event loop using boolean
print("event loop using boolean")
found = False
while not found:
    print("Hello")
    found = True
print()
#counting loop using while
print("counting loop using while")
x=1
while x <= 10:
    print("x= ", x)
    x= x+1
print()
#for, in loop looks at each letter:
print("for, in loop looks at each letter in a string or list")
sletters="abcdefghijk"
for let in sletters:
    print(let)
print()
#simple counting loop with range
print("simple counting loop with range")
for x in range (0,10):
    print ("x ",x)
print()
#simple counting loop with range reversed stepped
print("simple counting loop with range reversed")
for x in range (10, 0, -1):
    print("x= ", x)
print()

    
